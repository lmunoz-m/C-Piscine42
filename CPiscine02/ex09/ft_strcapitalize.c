char	*ft_strcapitalize(char *str)
{
	int	i;
	int	c;

	i = 0;
	c = 0;
	while (str[c] != '\0')
	{
		if (((str[c] >= 'a') && (str[c] <= 'z')) && (i == 0))
		{
			str[c] -= ('a' - 'A');
			i++;
		}
		else if (((str[c] >= 'A') && (str[c] <= 'Z')) && (i > 0))
		{
			str[c] += ('a' - 'A');
		}
		else if (((str[c] < '0') || (str[c] > '9')) && ((str[c] < 'A') \
			|| (str[c] > 'Z')) && ((str[c] < 'a') || (str[c] > 'z')))
			i = 0;
		else
			i++;
		c++;
	}
	return (str);
}
